<strong style="margin-left: 30px; font-family: verdana;"><h5>PATRON SAINTS</h5></strong><br>
<strong style="margin-left: 50px; font-family: verdana;"><h6>St. Arnold Janssen: SVD Founder</h6></strong><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Arnold Janssen was the second son born to Gerhard and Anna Katharina
Janssen. He grew up in a devoted Catholic family of seven children (five boys and two
girls).His father was a freight hauler, a man of deep faith who educated his children to
think beyond their own limited world-view and be aware of the missionary needs of the
Church around the world. Their family prayer included a nightly rosary, which was
followed by the Prologue of St. John’s Gospel “In the beginning was the Word and the
Word was with God and the Word was God.”</h6>
<br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >As a young man, Arnold developed an early interest in the natural sciences. He
went on to study in this field and qualified to be a high school teacher. Having
completed these studies, he went on to study theology and was ordained a diocesan
priest for the Diocese of Muenster on August 15, 1861. His first assignment was to teach in
a secondary school in Bocholt.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >At the age of 24, the scientist became a priest enriching the secular with the
sacred, the human with the Divine. His heart was filled with the desire to share Christ and
his Divine treasures to all people especially to those who had not heard of Him
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >On September 8, 1875, Arnold founded the religious order, “The Society of the
Divine Word (SVD). He also founded the Holy Spirit Missionary Sister (SSpS) and the Holy
Spirit Sisters of Perpetual Adoration (SSp-SAP).
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >On January 15, 1909, Arnold Janssen died at Steyl, Netherlands. He was beatified
in 1975. On October 5, 2003, at the Vatican before some 30,000 people, Pope John Paul
II declared Arnold Janssen, a Saint of the Church.
</h6>
<br>
<strong style="margin-left: 50px; font-family: verdana;"><h6> St. Joseph Freinademetz</h6></strong><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >St. Joseph Freinademetz, SVD, was born on April 15, 1852, the fourth child of
Giovanmattia and Anna Maria Freinademetz. The family eked out a living on their poor
and simple small farm as did their neighbors. Years later, the little farm house and quiet
hamlet of Oies in the Gader Valley changed when Joseph Freinademetz, SVD, was
beatified in 1975 by Pope Paul VI and then canonized a saint in the Roman Catholic
Church on October 5, 2003, by Pope John Paul II in Rome. Pilgrims regularly find their way
to pray at the small parish church of St. Martin where Joseph served as a curate for three
years. Or they visit the little mountain chapel high above the family farm or visit his
ancestral home. It is a surrounding that seems to speak of prayer and devotion
</h6>
<br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Joseph’s early years were uneventful. He helped with the farm chores, attended
daily Mass at his local parish and, on the advice of the parish priest, attended a school
some eleven-hour walk from his home. He eventually entered the major seminary and
was ordained a priest for the Brixon Diocese in 1875. He was initially assigned as a
teacher, but soon an article in the local diocesan newsletter about the new Mission
House at Steyl, Holland, founded by Fr.Arnold Janssen caught his attention. Joseph went 
4
to visit the Mission House. The visit was enough to convince Joseph that this was where he
could pursue his vocation as a missionary priest. He joined the fledgling group at Steyl in
1878, and barely a year later he received his mission cross along with Fr. John Baptist
Anzer, SVD. He had one more brief visit to his family home to say goodbye for the last
time, as he would never return to his homeland again. He was to be a missionary in
China. In 1881, the Mission House had received its own mission territory, the Province of
Shandong. Joseph was so devoted to his mission that, except to recover from an illness,
he never left Shandong.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >As so many missionaries have discovered, the grounding of their mission work is
first supported by a strong personal prayer life. Joseph had promoted this amongst the
clergy along with the words, “Do you imagine you can become holy without meditation,
something no saint was able to do? Without meditation life is lost.” He said his daily Mass
and prayed his Divine Office with the same intense dedication as he did with his
missionary work. Joseph had unwavering hope and belief in the power of God and the
sacraments. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >During such difficult times as the Boxer Rebellion in which two young Divine Word
Missionaries were martyred, he remained at his mission post. Well before his death, the
Chinese people and others with whom he worked recognized him as a saintly man for his
humility, for his firm yet gentle approach to his work, and for his total love of his people.
Toward the end of his all-too-few years, he was appointed the Provincial for the Society
of the Divine Word, a post he held until his death from typhus in 1908 at age 46.
</h6><br>

<strong style="margin-left: 50px; font-family: verdana;"><h6>Blessed Ma. Helena</h6></strong><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Blessed Maria Virgo (Helena Stollenwerk) was born in Rollesbroich, Germany on
November 28, 1852. While still at the little village elementary school she began
enthusiastically reading the annuals of the Holy Childhood Association, now the Papal
Work for Children, in her free time. They roused her determination to help the children of
China. When at the age of twenty she tried to follow her calling, she found no convent in
Germany that sent missionary sisters to China and for many years she searched in vain for
the address of such a congregation. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >During a visit to Steyl, she learned that Arnold Janssen saw the need to found a
congregation of missionary sisters although he could not promise that he would do so in
the near future. He offered her employment as a maid in the Mission House kitchen.
Helena was 30 years old when she accepted his offer. She hoped in that way to reach
her goal.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Two years later, in 1884, she was joined by Hendrina Stenmanns from Issum in the
German Lower Rhineland. For the first several years, the two women worked in the
kitchen and in the laundry and lived in a small, very simple house. Years later, when a
neighbouring convent became vacant, they moved there. On December 8, 1889
Helena, now Mother Maria and superior general, co-founded with St. Arnold, the
Missionary Sisters. From then on the development of the Congregation of the "Servants of
the Holy Spirit", as they are officially called, really took off. The rapidly growing community sent out the first sisters to Argentina in 1895; soon followed by a group sent to Togo. After
only seven years the Congregation numbered 100 sisters.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Arnold Janssen regarded prayer as an absolute necessity to support mission work.
He had long been thinking of a third, contemplative branch for his foundations. So on 8
December 1896, he clothed the first Adoration Sisters in their pink religious habit. In 1898
Mother Maria transferred to the cloistered Sisters becoming a novice with the name Sister
Maria Virgo. She would really have preferred to remain with the Missionary Sisters but
responded generously to Arnold Janssen's wish when he asked her to take the step.
Helena's dream, to go out to the great land of China, was never fulfilled. On February 3,
1900 Helen was stricken by a serious illness, she was admitted to religious profession as a
Holy Spirit Adoration Sister on her deathbed. On May 7, 1995, she was beatified by Pope
John Paul II.
</h6><br>

<strong style="margin-left: 50px; font-family: verdana;"><h6>Bl. Josepha Hendrina Stenmanns </h6></strong><br>
<strong><h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Mother Josepha Hendrina Stenmanns</strong> was born on 28 May 1852, in Issum, Germany, the
eldest of seven children. Even as a child she was concerned for the poor and the
suffering whom she visited with her mother. After leaving school, she contributed to the family income through her work as a silk weaver.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >At the age of 19, she joined the Franciscan Third Order, which nurtured in her a spirit of
simplicity and a deep prayer life. Her wish to consecrate herself to God increased as she
absorbed the Franciscan spirit, but the German Kulturkampf (which sought to subject the
Roman Catholic Church to State controls) made religious life impossible. When her
mother was dying, Hendrina promised to care for her siblings. It began to look as though
she would have to renounce the idea of religious life.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Some years later she found her way to Steyl, Holland, where German-born Fr Arnold
Janssen, due to the Kulturkampf, had gone to establish a centre to train priests for mission
work. Fr Janssen accepted Hendrina's request to be part of the Mission House as a
kitchen maid. Her real intention, however, was to support the mission cause by her work
in the kitchen. When she arrived in Steyl, she was almost 32 years old. She did not have
great plans, but simply wished to do what she recognized as God's will for her at each
moment. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Through her decision to live as a kitchen maid, Hendrina stepped down to the lowest
rung of the social ladder. A life of hard work and renunciation began that was to last five
years as she waited for the women's branch of the Mission House to be founded. On 8
December 1889, she and a few other women became postulants. The foundation of the
Missionary Sisters Servants of the Holy Spirit had been laid. The novitiate followed and
then in March 1894 she professed first vows and was given the name Josepha. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Sr Josepha was responsible for the practical matters in the house. Later on as directress of
postulants, she showed great understanding of human nature and was able to introduce
young women into religious life with wisdom and empathy. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Sr Josepha was known above all for her love of prayer; she progressed ever more into
interior quiet and true contemplation in the midst of her manifold tasks. She loved to pray
the rosary and short prayers, especially "Come, Holy Spirit" which became her "mantra",
leading her inward to the presence of God in the tabernacle of her heart.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >In spite of the burden of work and the demands made by a large, young community,
she did not lose herself in pure activity. In the depths of her heart she remained in union
with God and maintained her inner peace. For Mother Josepha, religious life meant
belonging to God entirely. The final months of Sr Josepha's life were marked by serious
and painful illness. On her deathbed, suffering from asthma, she bequeathed her spiritual
testament to the Sisters (every breath of a Servant of the Holy Spirit ought to be), "Come,
Holy Spirit". 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >She died on 20 May 1903 and was beatified in Steyl, by Cardinal José Saraiva Martins,
C.M.F., Prefect of Congregation for the Causes of Saints, on Sunday, 29 June 2008. 
</h6><br>

<strong style="margin-left: 50px; font-family: verdana;"><h6>THE FOUR SVD MARTYRS</h6></strong><br>
<strong><h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Luis (Ludwik) Mzyk</strong> (1905-1940) was martyred as novice master of the Polish Province.
After philosophy studies in St. Augustin he was sent to study in Rome where he was
ordained in 1932. He applied to work as missionary in Japan or China, but the superiors
appointed him for Poland where the SVD needed qualified personnel and where the
initial formation program had just began. So, he continued his stay in Collegio del Verbo
Divino, studied dogmatic theology at the Gregorian University, and finished his study with
the doctorate. In the Polish Province, he became novice master at Chludowo. Short after
the beginning of the war, he was arrested and put in the maximum security prison in
Poznan where he was cruelly killed on February 20, 1940.
</h6><br>
<strong><h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Gregory Frackowiak</strong> (1911-1943). He is the youngest one among the four martyrs and
was beheaded in the prison in Dresden on May 5, 1943 when he was 32 years old. As
receptionist and bookbinder he showed a special sensitivity to the poor who used to
come to him asking not only for material help but also spiritual help. At the beginning of
the war in 1939, the SVD house in Górna Grupa where he stayed was closed down and
confiscated and the confreres were expelled. He went to his family. Some people were
involved in distributing the fliers against the Hitler’s dictatorship. For some time he was
also involved in this action but after a time he stopped doing it. The group was
discovered and many persons were arrested. It was clear that some would be
sentenced to death. Bro. Gregory, after consultation with his confessor, freely presented
himself to the police and took responsibility for the distribution of the fliers. Due to his
sacrifice some fathers of families were allowed to rejoin their families. Brother Gregory
was brought to Dresden where he was beheaded. Many others met a similar death not
only in the Hitler’s time but also in the first years in East Germany under communism.
</h6><br>
<strong><h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Stanislaus Kubista</strong>(1898-1940). He was a mission animator and communicator. His
contribution to mission animation is amazing. As director of the SVD printing press in
Poland he shared the Founder’s enthusiasm for the printing and editorial apostolates. He
edited two missionary magazines, wrote articles and published a book. After his arrest, he
was sent to the concentration camp at Sachsenhausen where he was killed with 
7
extreme cruelty. According to witnesses who survived, he died maintaining his priestly
and religious dignity.
</h6><br>
<strong><h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Aloysius Liguda</strong>(18989-1942). He was good educator and was often sought out as a
spiritual director. As chaplain in the schools he was appreciated and loved by young
people. His conferences during recollections and retreats were valued. One of his books
of conferences went into three printings. Our reading for the Office of Readings today is
taken from his writings. A man of great tranquility and with a sense of humor, Aloysius was
of enormous help to other prisoners in the concentration camp in Dachau. In a situation
of human horror, he was able to help people spiritually and psychologically and
encouraged them not to give up. Along with nine other prisoners, he was drowned in
Dachau on December 9, 1942.
</h6><br>
<strong style="margin-left: 30px; font-family: verdana;"><h5>THE WORK OF THE DIVINE WORD MISSIONARIES IN THE PHILIPPINES
</h5></strong><br>
<h6 style="margin-left: 70px; margin-right: 70px;font-family: verdana;" >The Philippines was a fertile ground for the seed of God to grow and mature.
Saint Arnold Janssen realized this through his contact with Fr. Eberhard Limbrock, the
Prefect Apostolic to New Guinea. In 1909, shortly before the death of the founder, he
considered sending missionaries to Abra upon the request of the Archbishop of Nueva
Segovia.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >At present, the seed once sown has continuously flourished. The Divine Word
Missionaries have spread to all parts of the country from north to south (Abra, Cagayan,
Manila, Mindoro, Legazpi, Visayas and Mindanao) and are engaged in varied forms of
apostolate: mass media, parish, social action, retreats and spiritual renewal, and
educational formation of religious, priests, and laymen.
