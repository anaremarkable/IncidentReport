<strong><h1 style="text-align: center; font-family: verdana;" >A Glimpse of DWCL
</h1></strong><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Through the years, Divine Word College of Legazpi continues to produce some of
the best leaders and professionals in the province. Its legacy began when the Liceo de
Albay, an exclusive school for boys, was established right after the World War II in 1947
and was managed by a group of clergy of the Diocese of Caceres. The administration
was entrusted to Fr. Juan Carullo in 1947 and in a few years of operation the school
gained its good reputation. However, the operation was hastily jeopardized as the strong
typhoon Trix devastated the Bicolandia including Legazpi City in October 1952, a year
after Legazpi was established as a Diocese. With the languishing condition of Liceo de
Albay, Fr. Carullo, a retired Army Chaplain, invested his personal money and borrowed
from local people to rebuild the devastated building. With his being willful, Liceo de
Albay was rebuilt and continued its educational operation. Still, Fr. Carullo confessed that
he was financially exhausted to sustain the struggle of the school apart from being
physically tired. At just about the same time, the Societas Verbi Divini (SVD), a Catholic
missionary religious congregation founded by St. Arnold Janssen, was looking for an ideal
place in the Bicol Region for missionary expansion. These circumstances seemingly
dovetailed Liceo de Albay and SVD to one another. The SVD lobbied with the bishop,
Most Reverend Flaviano B. Ariola, D.D., for the transfer of the Liceo from the diocese to
the SVD Congregation. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >In 1961, the SVD officially took over the management of Liceo de Albay and
handed over the administration to Fr. Joseph Bates, SVD, with Msgr. Honesto A.
Moraleda, a diocesan clergy, as Principal. The new school kept its exclusivity for the
education of boys with the help of Msgr. Nestor C. Cariño and Frt. Florante Camacho,
SVD as Regent. Fr. Bates raised more funds from the SVDs, his family and friends to start
the reconstruction and the development of the Liceo de Albay. As he was determined
to expand the Liceo, he valiantly acquired a five-hectare lot in a year’s time of his
administration, despite financial struggles. Indeed, the construction of a school building
to accommodate the growing population was his top priority as the school began
offering the grade school program. 
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >In 1965, the school raised its status to tertiary education, offering liberal arts,
commerce, education and secretarial programs. Five years later, Fr. Joseph Bates
initiated the construction of a new building in the acquired property. Through concerted
effort and with the new President, Fr. Florante Camacho,SVD, the Basic Education
Department transferred to the new three-story building in Washington Drive, Legazpi City,
now called the North Campus in 1977. A year after the transfer of basic education, the
Graduate School of Business Management was opened and offered the Master in
Business Administration program. In 1980, the DWCL was one of the thirteen charter–
member institutions of the Bicol Conference for Higher Education, the forerunner of the
Bikol Foundation for Higher Education.
</h6><br>
<h6 style="margin-left: 70px; margin-right: 70px; font-family: verdana;" >Since then, DWCL has been offering more Degree Programs to cater to the need
for more professionals and human resources of the growing number of offices and
agencies in various fields. In the passing of time, the administration continually provided
state-of–the-art facilities and additional buildings to cater to the needs of the institution’s
stakeholders. Faithful to the teachings and traditions of the Church, under the inspiration
of St. Arnold Janssen, the DWCL continuously serves the people of God by working
together in faith, in response to the needs of the region, the country and the world.
